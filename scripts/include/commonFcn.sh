# FUNCTION DEFINITIONS FOR OTHER RUN SCRIPTS

# FUNCTION TO ECHO INPUT IN GREEN
# >>>-------------------------------------------------------------
# INPUTS:
# $1: INPUT TO ECHO
# ----------------------------------------------------------------
EchoGreen(){
    echo -e "\e[32m$1\e[0m"
}
